import { useState } from "react";
import { useRoute, Link, useLocation } from "wouter";
import { Button } from "@/components/ui/button";
import { Card, CardContent, CardHeader, CardTitle, CardDescription } from "@/components/ui/card";
import { useGetSession, useGenerateReport, ScreeningResult } from "@workspace/api-client-react";
import { generateReportPdf } from "@/lib/pdf-generator";
import { Heart, MessageCircle, FileText, AlertTriangle, Share2, Mail, X, Send } from "lucide-react";
import { useToast } from "@/hooks/use-toast";

export default function ResultsPage() {
  const [, params] = useRoute("/results/:sessionId");
  const sessionId = params?.sessionId ? parseInt(params.sessionId) : 0;
  const [, setLocation] = useLocation();
  const { toast } = useToast();
  const [showEmailModal, setShowEmailModal] = useState(false);
  const [therapistEmail, setTherapistEmail] = useState("");
  const [senderNote, setSenderNote] = useState("");

  const { data: session, isLoading, error } = useGetSession(sessionId, {
    query: { enabled: !!sessionId, queryKey: ['session', sessionId] }
  });

  const generateReport = useGenerateReport();

  if (isLoading) {
    return (
      <div className="flex-1 flex items-center justify-center">
        <div className="animate-pulse flex flex-col items-center gap-4">
          <Heart className="w-8 h-8 text-primary animate-bounce" />
          <p className="text-muted-foreground">Preparing your insights...</p>
        </div>
      </div>
    );
  }

  if (error || !session || !session.results) {
    return (
      <div className="flex-1 flex items-center justify-center p-4">
        <Card className="max-w-md w-full glass-panel">
          <CardContent className="pt-6 text-center space-y-4">
            <AlertTriangle className="w-12 h-12 text-destructive mx-auto" />
            <h2 className="text-xl font-bold">Something went wrong</h2>
            <p className="text-muted-foreground">We couldn't load your results.</p>
            <Button onClick={() => setLocation("/")}>Go Home</Button>
          </CardContent>
        </Card>
      </div>
    );
  }

  const results = session.results as unknown as ScreeningResult;

  const handleShare = async () => {
    const url = window.location.href;
    if (navigator.share) {
      await navigator.share({ title: "My MindCheck Results", url });
    } else {
      await navigator.clipboard.writeText(url);
      toast({ title: "Link copied!", description: "Share it with someone you trust." });
    }
  };

  const handleGenerateReport = () => {
    generateReportPdf(session);
    generateReport.mutate({ id: sessionId }, {
      onSuccess: () => {
        toast({ title: "Report generated", description: "Your PDF has been downloaded and sent securely." });
      }
    });
  };

  const handleSendToTherapist = () => {
    if (!therapistEmail.trim()) {
      toast({ title: "Enter an email", description: "Please enter your therapist's email address.", variant: "destructive" });
      return;
    }

    const moduleLines = results.modules
      .map(m => `• ${m.name}: ${m.riskLevel} (${Math.round(m.riskPercent)}%)`)
      .join("\n");

    const recommendationLines = results.recommendations
      .map((r, i) => `${i + 1}. ${r}`)
      .join("\n");

    const shareUrl = window.location.href;
    const date = new Date().toLocaleDateString("en-US", { year: "numeric", month: "long", day: "numeric" });

    const subject = encodeURIComponent(`MindCheck Wellness Report — ${session.name}, ${date}`);

    const body = encodeURIComponent(
`Hi,

My name is ${session.name}${session.age ? `, age ${session.age}` : ""}. I completed a mental health screening on MindCheck and wanted to share my results with you ahead of our session.

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
OVERALL: ${results.riskLevel} (${results.overallScore}%)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

MODULE BREAKDOWN:
${moduleLines}

RECOMMENDATIONS FROM SCREENING:
${recommendationLines}
${results.safetyAlert ? "\n⚠️  SAFETY NOTE: This screening flagged potential distress. Please review at your earliest convenience.\n" : ""}
Full interactive report: ${shareUrl}
${senderNote ? `\nA note from me:\n${senderNote}` : ""}

Generated by MindCheck — mindcheck.replit.app`
    );

    window.location.href = `mailto:${therapistEmail}?subject=${subject}&body=${body}`;
    setShowEmailModal(false);
    toast({ title: "Email app opened!", description: "Review and send the pre-filled report to your therapist." });
  };

  const getRiskColor = (level: string) => {
    switch (level) {
      case "High Risk": return "text-destructive border-destructive/20 bg-destructive/10";
      case "Needs Attention": return "text-orange-600 border-orange-500/20 bg-orange-500/10 dark:text-orange-400";
      case "Keep an Eye On It": return "text-yellow-600 border-yellow-500/20 bg-yellow-500/10 dark:text-yellow-400";
      default: return "text-emerald-600 border-emerald-500/20 bg-emerald-500/10 dark:text-emerald-400";
    }
  };

  return (
    <div className="flex-1 p-4 md:p-8 max-w-5xl mx-auto w-full space-y-8 animate-in fade-in slide-in-from-bottom-8 duration-700">
      {/* Email to Therapist Modal */}
      {showEmailModal && (
        <div className="fixed inset-0 z-50 flex items-center justify-center p-4">
          <div className="absolute inset-0 bg-black/50 backdrop-blur-sm" onClick={() => setShowEmailModal(false)} />
          <Card className="relative w-full max-w-md glass-panel border-0 shadow-2xl z-10 animate-in zoom-in-95 duration-200">
            <CardHeader className="pb-2">
              <div className="flex items-center justify-between">
                <div className="flex items-center gap-3">
                  <div className="w-10 h-10 rounded-2xl bg-primary/20 flex items-center justify-center">
                    <Mail className="w-5 h-5 text-primary" />
                  </div>
                  <div>
                    <CardTitle className="text-lg">Send to Therapist</CardTitle>
                    <CardDescription>Your results will be pre-filled in your email app</CardDescription>
                  </div>
                </div>
                <Button variant="ghost" size="icon" className="rounded-xl" onClick={() => setShowEmailModal(false)}>
                  <X className="w-4 h-4" />
                </Button>
              </div>
            </CardHeader>
            <CardContent className="space-y-4 pt-2">
              <div className="space-y-2">
                <label className="text-sm font-medium text-foreground/80">Therapist's Email *</label>
                <input
                  type="email"
                  placeholder="therapist@example.com"
                  value={therapistEmail}
                  onChange={e => setTherapistEmail(e.target.value)}
                  className="w-full px-4 py-3 rounded-xl bg-white/50 dark:bg-black/30 border border-white/20 text-foreground placeholder:text-muted-foreground focus:outline-none focus:ring-2 focus:ring-primary/50 text-sm"
                  onKeyDown={e => e.key === "Enter" && handleSendToTherapist()}
                />
              </div>
              <div className="space-y-2">
                <label className="text-sm font-medium text-foreground/80">Add a personal note <span className="text-muted-foreground font-normal">(optional)</span></label>
                <textarea
                  placeholder="e.g. I've been struggling with sleep a lot lately..."
                  value={senderNote}
                  onChange={e => setSenderNote(e.target.value)}
                  rows={3}
                  className="w-full px-4 py-3 rounded-xl bg-white/50 dark:bg-black/30 border border-white/20 text-foreground placeholder:text-muted-foreground focus:outline-none focus:ring-2 focus:ring-primary/50 text-sm resize-none"
                />
              </div>
              <div className="bg-primary/5 border border-primary/10 rounded-xl p-3 text-xs text-muted-foreground">
                📋 Your overall score, module breakdown, recommendations{results.safetyAlert ? ", and safety flag" : ""} will be included. Clicking send will open your default email app.
              </div>
              <div className="flex gap-3 pt-1">
                <Button variant="outline" className="flex-1 rounded-xl" onClick={() => setShowEmailModal(false)}>
                  Cancel
                </Button>
                <Button className="flex-1 rounded-xl" onClick={handleSendToTherapist}>
                  <Send className="w-4 h-4 mr-2" />
                  Open Email App
                </Button>
              </div>
            </CardContent>
          </Card>
        </div>
      )}

      <div className="flex flex-col md:flex-row md:items-center justify-between gap-6">
        <div>
          <h1 className="text-3xl md:text-4xl font-display font-bold mb-2">Hey {session.name}, here's where you're at.</h1>
          <p className="text-muted-foreground text-lg">Thank you for being honest. It takes courage.</p>
        </div>
        <div className="flex flex-wrap gap-3">
          <Button variant="outline" onClick={handleShare} className="rounded-xl border-white/20 bg-white/40 dark:bg-black/40 backdrop-blur-md">
            <Share2 className="w-4 h-4 mr-2" />
            Share Results
          </Button>
          <Button variant="outline" onClick={() => setShowEmailModal(true)} className="rounded-xl border-white/20 bg-white/40 dark:bg-black/40 backdrop-blur-md">
            <Mail className="w-4 h-4 mr-2" />
            Send to Therapist
          </Button>
          <Button variant="outline" onClick={handleGenerateReport} className="rounded-xl border-white/20 bg-white/40 dark:bg-black/40 backdrop-blur-md">
            <FileText className="w-4 h-4 mr-2" />
            Download PDF
          </Button>
          <Link href={`/chat/${session.conversationId || 'new'}?sessionId=${session.id}`}>
            <Button className="rounded-xl shadow-lg">
              <MessageCircle className="w-4 h-4 mr-2" />
              Talk to MindBot
            </Button>
          </Link>
        </div>
      </div>

      {results.safetyAlert && (
        <div className="bg-destructive/10 border-l-4 border-destructive p-6 rounded-r-2xl animate-in slide-in-from-left-4">
          <div className="flex items-start gap-4">
            <AlertTriangle className="w-6 h-6 text-destructive shrink-0" />
            <div>
              <h3 className="font-bold text-destructive text-lg">We noticed you're in a tough spot</h3>
              <p className="text-destructive/80 mt-1">Some of your answers indicate you might be thinking about self-harm. You matter, and there is help available right now.</p>
              <div className="mt-4 flex gap-4">
                <Button variant="destructive" className="rounded-xl font-bold">Call 988</Button>
                <Button variant="outline" className="rounded-xl border-destructive/20 text-destructive hover:bg-destructive/20">Text HOME to 741741</Button>
              </div>
            </div>
          </div>
        </div>
      )}

      <div className="grid grid-cols-1 md:grid-cols-3 gap-6">
        <Card className="md:col-span-1 glass-panel border-0 shadow-xl overflow-hidden flex flex-col justify-center items-center text-center p-8">
          <h3 className="text-sm font-medium text-muted-foreground uppercase tracking-wider mb-4">Overall Vibe</h3>
          <div className={`text-3xl font-display font-bold px-6 py-3 rounded-2xl border ${getRiskColor(results.riskLevel)} mb-4`}>
            {results.riskLevel}
          </div>
          <p className="text-muted-foreground">Based on your responses across all modules.</p>
        </Card>

        <Card className="md:col-span-2 glass-panel border-0 shadow-xl overflow-hidden">
          <CardHeader>
            <CardTitle>Recommendations</CardTitle>
            <CardDescription>Small steps you can take today</CardDescription>
          </CardHeader>
          <CardContent className="space-y-4">
            {results.recommendations.map((rec, i) => (
              <div key={i} className="flex items-start gap-3 bg-white/40 dark:bg-black/40 p-4 rounded-2xl border border-white/10">
                <div className="w-6 h-6 rounded-full bg-primary/20 text-primary flex items-center justify-center shrink-0 text-sm font-bold mt-0.5">{i + 1}</div>
                <p className="text-foreground/90">{rec}</p>
              </div>
            ))}
          </CardContent>
        </Card>
      </div>

      <div className="space-y-4">
        <h2 className="text-2xl font-display font-bold">The Breakdown</h2>
        <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 gap-4">
          {results.modules.map(m => (
            <Card key={m.name} className="glass-panel border-0 hover:shadow-lg transition-all">
              <CardContent className="p-6">
                <div className="flex justify-between items-start mb-4">
                  <h3 className="font-semibold text-lg">{m.name}</h3>
                  <span className={`text-xs font-bold px-2 py-1 rounded-full border ${getRiskColor(m.riskLevel)}`}>
                    {m.riskLevel}
                  </span>
                </div>
                <div className="space-y-2">
                  <div className="flex justify-between text-sm">
                    <span className="text-muted-foreground">Score</span>
                    <span className="font-medium">{m.score} / {m.maxScore}</span>
                  </div>
                  <div className="h-2 w-full bg-white/50 dark:bg-black/50 rounded-full overflow-hidden">
                    <div
                      className="h-full bg-primary transition-all duration-1000"
                      style={{ width: `${m.riskPercent}%` }}
                    />
                  </div>
                </div>
              </CardContent>
            </Card>
          ))}
        </div>
      </div>
    </div>
  );
}
